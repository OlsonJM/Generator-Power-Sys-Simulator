    private void bGen3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGen3ActionPerformed
        if(gen3Run){
            bGen3.setIcon(new javax.swing.ImageIcon(getClass().getResource("/prodsim/Gen_Green.JPG")));
            g3LineBus.setBackground(java.awt.Color.green);
            gen3Run = false;
            log("Generator 3 Shutdown");
            //trip generator breaker
            if(bkrG3){
                bBkrGen3.setIcon(new javax.swing.ImageIcon(getClass().getResource("/prodsim/Breaker_Green.JPG")));
                bkrG3 = false;
                log("Breaker G3 Tripped due to Generator Shutdown");
            }
            this.repaint();
        }else {
            //log("Generator 3 Starting");
            bGen3.setIcon(new javax.swing.ImageIcon(getClass().getResource("/prodsim/Gen_Red.JPG")));
            g3LineBus.setBackground(java.awt.Color.red);
            gen3Run = true;
            log("Generator 3 Running");
            this.repaint();
        }
        setBuses();
}//GEN-LAST:event_bGen3ActionPerformed

