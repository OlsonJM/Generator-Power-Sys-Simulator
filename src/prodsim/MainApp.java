    private void bSysAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSysAutoActionPerformed
        
        //Check generators are assigned to only one location each
        if(techBusPref==utilBusPref||techBusPref==backupPref||utilBusPref==backupPref)
        {
            log("Cannot Enter Auto - Check Preferred Generator Selections");
            bSysManual.setSelected(true);
            sysInAuto = false;
            return;
        }
        
        //check generators are in auto
        if(!(gen1Mode==3&&gen2Mode==3&&gen3Mode==3))
        {
            log("Cannot Enter Auto - Check Generator Mode");
            bSysManual.setSelected(true);
            sysInAuto = false;
            return;
        }

        //Check bus arrangements - Ensures GA and GB are open
        if((busArrangement==1&&!(bkrT3&&bkrT1))||
                (busArrangement==2&&!(bkrT1&&bkrT2))||
                (busArrangement==3&&!(bkrT3&&bkrT2)))
        {
            log("Cannot Enter Auto - Undefined bus State. Check breakers and bus arrangement setting");
            bSysManual.setSelected(true);
            return;
        }

        //check Generator Tie breakers
        if(bkrG1A||bkrG1B||bkrG2A||bkrG2B||bkrG3A||bkrG3B)
        {
            log("Cannot Enter Auto - Undefined bus State. Check generator Tie breakers");
            bSysManual.setSelected(true);
            return;

        }

        //check Generator breakers
        if(bkrG1||bkrG2||bkrG3)
        {
            log("Cannot Enter Auto - Undefined bus State. Check generator breakers");
            bSysManual.setSelected(true);
            return;

        }

        //OK to go to auto if Gen's are running, but now shut them down
        if(gen1Run)
            bGen1ActionPerformed(null);
        if(gen2Run)
            bGen2ActionPerformed(null);
        if(gen3Run)
            bGen3ActionPerformed(null);

        sysInAuto = true;
        log("System Mode set to Auto");
        bManualIndicator.setBackground(Color.white);
        bAutoIndicator.setBackground(Color.red);
        checkSources();
    }//GEN-LAST:event_bSysAutoActionPerformed

